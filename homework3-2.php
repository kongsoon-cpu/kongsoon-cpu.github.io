<?php
$n = 30;
$arr = [];

// 1. 랜덤 숫자 생성 (1~1000)
for ($i = 0; $i < $n; $i++) {
    $arr[$i] = rand(1, 1000);
}

// 2. 생성된 배열 출력
echo "<h2>생성된 숫자</h2>";
for ($i = 0; $i < $n; $i++) {
    echo $arr[$i] . " ";
}

// 3. 정렬
sort($arr);

// 4. 정렬된 배열 출력
echo "<br><br><h2>정렬된 숫자 (오름차순)</h2>";
for ($i = 0; $i < $n; $i++) {
    echo $arr[$i] . " ";
}
?>